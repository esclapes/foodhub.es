     * @param  Request  $request
     * @return Response
     */
    public function store(Requests\CreateOrderRequest $request)
    {
        $order = \App\Order::create($request->only(['name', 'description', 'closing_at']));

        return redirect()->action('OrdersController@show', ['id' => $order->id]);
    }

